package com.github.wulfaz.android.openkarotz.database;

import android.content.Context;

import androidx.room.Database;
import androidx.room.Room;
import androidx.room.RoomDatabase;
import androidx.room.TypeConverters;

/**
 * Room database for the OpenKarotz application.
 */
@Database(
    entities = {KarotzDevice.class},
    version = 1,
    exportSchema = false
)
@TypeConverters({DatabaseConverters.class})
public abstract class AppDatabase extends RoomDatabase {
    
    private static final String DATABASE_NAME = "openkarotz_database";
    private static AppDatabase INSTANCE;
    
    public abstract KarotzDeviceDao karotzDeviceDao();
    
    public static AppDatabase getInstance(Context context) {
        if (INSTANCE == null) {
            synchronized (AppDatabase.class) {
                if (INSTANCE == null) {
                    INSTANCE = Room.databaseBuilder(
                            context.getApplicationContext(),
                            AppDatabase.class,
                            DATABASE_NAME
                    )
                    .addCallback(new DatabaseCallback())
                    .build();
                }
            }
        }
        return INSTANCE;
    }
    
    private static class DatabaseCallback extends RoomDatabase.Callback {
        // Add any database initialization logic here
    }
}
